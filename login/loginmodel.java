package id.ac.uns.vokasi.d3ti.login;

public class loginmodel {
String email, pass, nama, ttl, nohp, jk ;
	
	public void setEmail(String email) {
		this.email=email;
	}
	public void setPass(String pass) {
		this.pass=pass;
	}
	public void setNama(String nama) {
		this.nama=nama;
	}
	public void setTtl(String ttl) {
		this.ttl=ttl;
	}
	public void setNohp(String nohp) {
		this.nohp=nohp;
	}
	public void setJk(String jk) {
		this.jk=jk;
	}
	
	public String getEmail() {
		return email;
	}
	public String getPass() {
		return pass;
	}
	public void getNama(String nama) {
		this.nama=nama;
	}
	public void getTtl(String ttl) {
		this.ttl=ttl;
	}
	public void getNohp(String nohp) {
		this.nohp=nohp;
	}
	public void getJk(String jk) {
		this.jk=jk;
	}
	
}